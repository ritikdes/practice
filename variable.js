/*
Declare three variables: one using var, one using let, and one using const.
Try reassigning values to them and note any errors or behaviors.
*/

//Declaring three variables var,let and const
var num1 = 50
let num2 = 60
const num3 = 70

//Displaying the variables
console.log(num1)
console.log(num2)
console.log(num3)

//Reassigning the values
var num1 = 50 //Reassigning value for var
num2 = 60  //Reassigning value for let
num3 = 70 //Reassigning value for const which will cause assignment error

//Re-displaying the variables
console.log(num1)
console.log(num2)
console.log(num3)